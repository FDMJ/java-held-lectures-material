package de.tutego.appliances;

/**
 * Lernziel: Vererbung
 * - Assoziation vs. Vererbung
 * - Generalisierung/Spezialisierung
 * - Schlüsselwort extends
 * - Was vererbt wird, Sichtbarkeit protected
 * - Einfachvererbung und keine Mehrfachvererbung (auf Klassenebene)
 * - java.lang.Object
 *
 * @see SuperConstructor
 */
public class Inheritance /*extends Object*/ {
  public static void main( String[] args ) {
    /*
    Beispiele für Assoziationen:
    - Eine Tasse *hat* einen Henkel
    - Ein Vogel *kennt* sein Nest
    - Ein Haus *enthält* Radios
     */
    /*
    Beispiele für Vererbung:
    - Ein Tasse *ist (eine Art)* Gefäß
    - Ein Hund *ist* ein Säugetier
    - Ein Mensch *ist* ein Säugetier
     */
    /*
                         ^         |
        Generalisierung  |         |  Spezialisierung
                         |         v

     */
  }
}